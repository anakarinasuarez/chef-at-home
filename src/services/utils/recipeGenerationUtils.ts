/**
 * Obtiene la imagen para una receta
 * Función pura que solo se encarga de obtener imágenes
 */
export const getRecipeImage = async (
  title: string,
  ingredients: string[]
): Promise<string | null> => {
  try {
    const { getRecipeImage: getImage } = await import("./recipeService");
    return await getImage(title, ingredients);
  } catch (error) {
    console.error("Error getting recipe image:", error);
    return null;
  }
};

/**
 * Agrega imagen a una receta
 * Función pura que transforma una receta agregando su imagen
 */
export const addImageToRecipe = async (
  recipe: CustomRecipe,
  ingredients: string[],
  source: string
): Promise<RecipeWithImage> => {
  const image = await getRecipeImage(recipe.title, ingredients);

  return {
    ...recipe,
    image,
    source,
  };
};

/**
 * Genera recetas múltiples usando OpenAI
 * Función especializada para generación múltiple con OpenAI
 */
export const generateMultipleWithOpenAI = async (
  params: RecipeGenerationParams & { count: number }
): Promise<RecipeWithImage[]> => {
  try {
    const { generateRecipeWithOpenAI } = await import("./openaiRecipeService");
    const { isOpenAIServiceAvailable } = await import("./openaiRecipeService");

    if (!isOpenAIServiceAvailable()) {
      throw new Error("OpenAI service not available");
    }

    console.log("🎯 Using OpenAI GPT-4 for multiple recipe generation");
    const response = await generateRecipeWithOpenAI({
      ingredients: params.ingredients,
      servings: params.servings,
      cuisine: params.cuisine,
      count: params.count,
    });

    if (!response.recipes || response.recipes.length === 0) {
      throw new Error("No recipes generated by OpenAI");
    }

    // Procesar imágenes en paralelo
    const recipesWithImages = await Promise.all(
      response.recipes.map(async (recipe) => {
        try {
          return await addImageToRecipe(
            recipe as CustomRecipe,
            params.ingredients,
            "openai-gpt4"
          );
        } catch (error) {
          console.error("Error getting image for recipe:", error);
          return {
            ...recipe,
            image: null,
            source: "openai-gpt4",
          } as RecipeWithImage;
        }
      })
    );

    return recipesWithImages;
  } catch (error) {
    throw new Error(
      `OpenAI multiple generation failed: ${
        error instanceof Error ? error.message : "Unknown error"
      }`
    );
  }
};

