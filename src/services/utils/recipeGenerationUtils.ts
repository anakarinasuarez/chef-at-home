/**
 * Estrategia de fallback para generación de recetas
 * Función pura que maneja el fallback cuando los servicios principales fallan
 */
export const generateFallbackRecipe = async (
  params: RecipeGenerationParams
): Promise<RecipeWithImage> => {
  const { generateCustomRecipe } = await import("./recipeService");

  console.log("🔄 Using fallback recipe generation");
  const recipe = generateCustomRecipe(params.ingredients, params.servings);

  return {
    ...recipe,
    image: null,
    source: "fallback-template",
  };
};

