/**
 * Utilidades para generación de recetas - Enfoque funcional
 * Divide la lógica compleja en funciones más pequeñas y especializadas
 */

import { CustomRecipe } from "./recipeService";
import { ParsedRecipe } from "./geminiService";

// Tipos para las funciones utilitarias
interface RecipeGenerationParams {
  ingredients: string[];
  servings: number;
  cuisine: string;
}

interface RecipeWithImage extends CustomRecipe {
  image: string | null;
  source: string;
}

interface RecipeGenerationResult {
  success: boolean;
  recipe?: RecipeWithImage;
  error?: string;
}

/**
 * Genera una receta usando OpenAI GPT-4
 * Función pura que solo se encarga de la generación con OpenAI
 */
export const generateWithOpenAI = async (
  params: RecipeGenerationParams
): Promise<RecipeGenerationResult> => {
  try {
    const { generateRecipeWithOpenAI } = await import("./openaiRecipeService");
    const { isOpenAIServiceAvailable } = await import("./openaiRecipeService");

    if (!isOpenAIServiceAvailable()) {
      return { success: false, error: "OpenAI service not available" };
    }

    console.log("🎯 Using OpenAI GPT-4 for recipe generation");
    const response = await generateRecipeWithOpenAI({
      ingredients: params.ingredients,
      servings: params.servings,
      cuisine: params.cuisine,
      count: 1,
    });

    if (!response.recipes || response.recipes.length === 0) {
      return { success: false, error: "No recipes generated by OpenAI" };
    }

    return { success: true, recipe: response.recipes[0] as RecipeWithImage };
  } catch (error) {
    return {
      success: false,
      error:
        error instanceof Error ? error.message : "OpenAI generation failed",
    };
  }
};

